static bool cmp(vll &a, vll &b)
{
    if (a[0] / sqrtn != b[0] / sqrtn)
        return (a[0] / sqrtn) < (b[0] / sqrtn);
    return a[1] < b[1];
}
void add(ll idx)
{
    freq[v[idx]]++;
    if (freq[v[idx]] == 1)
        cnt++;
}
void remove(ll idx)
{
    freq[v[idx]]--;
    if (freq[v[idx]] == 0)
        cnt--;
}

void help(vector<vll> &q)
{

    ll left = 0, right = -1;

    for (auto &x:q)
    {
        ll l = x[0];
        ll r =x[1];
        while (right < r)
            add(++right);
        while (right > r)
            remove(right--);
        while (left > l)
            add(--left);
        while (left < l)
            remove(left++);
        ans[x[2]] = cnt;
    }
}

